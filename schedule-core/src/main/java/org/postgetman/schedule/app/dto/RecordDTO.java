package org.postgetman.schedule.app.dto;

public class RecordDTO {

    private String date;

    private String time;

    public RecordDTO(){

    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
